//Function which takes in a value and returns whether the input is a prime number (calculated by repeated division)
bool IsPrime(int TestValue){
	
	if(TestValue == 1){
		return false;
	}
	
	if(TestValue == 2){
		return true;
	}
		
	for(int i = 2; i <= pow(TestValue, 0.5); i++){
		if(TestValue % i == 0){
			return false;
		}
	}
	
	return true;
}


int main() {
	
	//Start with 3x3 ulam spiral as basecase
	int NumberOfPrimes = 3;
	int SideLength = 2;
	int CurrentDiagValue = 9;
	
	//Generate the values of the entries on the diagonals and determine whether they're prime, add to counter if so
	while((1.0*NumberOfPrimes)/(2*SideLength+1) > 0.1){
		SideLength += 2;
		for(int i = 0; i < 3; i++){
			CurrentDiagValue += SideLength;
			if(IsPrime(CurrentDiagValue)){
				NumberOfPrimes++;
			}
		}
		CurrentDiagValue += SideLength;
	}
	
	std::cout << "The size of Ulam Spiral where the ratio of primes falls below 10% is " << (SideLength+1) << "\n";
	
}
